/// Modem config element
typedef struct dev_conf {
	int vid;		///< vendor id
	int pid;		///< product id
	char type;		///< modem type - 'C' for CDMA, 'W' for GPRS/UMTS/LTE, 'T' (unused) for TD-SCDMA
	int data_port;	///< main port for data communication number
	int data_cls;	///< interface class (used if device class == 0)
	int data_sub;	///< interface subclass
	int data_prot;	///< protocol type
	int ui_port;	///< user interface port for modem controlling, status sms, ussd and so on
	int ui_cls;		///< interface class (used if device class == 0)
	int ui_sub;		///< interface subclass
	int ui_prot;	///< protocol type
} dev_conf_t;

